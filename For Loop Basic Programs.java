/* print 1 to 10 using for for loop */
public class Main {
    public static void main(String[] args) {
       for(int i=1;i<=10;i++){
           System.out.print(i);
       }
    }}

/* print i 10 times using for for loop */
public class Main {
    public static void main(String[] args) {
       for(int i=1;i<=10;i++){
           System.out.print(" "+"i");
       }
    }}
